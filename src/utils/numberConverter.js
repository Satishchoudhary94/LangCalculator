export const convertNumber = (number, language) => {
  if (language === 'en') return number;
  
  const numStr = number.toString();
  let result = '';
  
  for (let i = 0; i < numStr.length; i++) {
    if (numStr[i] === '.') {
      result += '.';
    } else {
      result += numberWords[language][parseInt(numStr[i])];
    }
  }
  
  return result;
};

